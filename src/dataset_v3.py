import os
import torch
import numpy as np
from torch.utils.data import Dataset, DataLoader
import torch.nn.functional as F
import matplotlib.pyplot as plt

class FluidDatasetV3(Dataset):
    def __init__(self, data_dir="fluid_dataset_2"):
        self.data_dir = data_dir
        # Grab all the chaotic simulations and sort them
        self.files = sorted([f for f in os.listdir(data_dir) if f.endswith('.npz')])
        print(f"Linked to {len(self.files)} simulations in {data_dir}.")

    def __len__(self):
        return len(self.files)

    def __getitem__(self, idx):
        # Load the raw NumPy arrays generated by your physics engine
        filepath = os.path.join(self.data_dir, self.files[idx])
        data = np.load(filepath)
        
        # Convert to PyTorch Tensors and add a channel dimension [Channels, H, W]
        sdf = torch.tensor(data['sdf'], dtype=torch.float32).unsqueeze(0) 
        u = torch.tensor(data['u'], dtype=torch.float32).unsqueeze(0)     
        v = torch.tensor(data['v'], dtype=torch.float32).unsqueeze(0)     
        
        # Stack the target velocity fields together into one [2, 64, 64] tensor
        target_64 = torch.cat([u, v], dim=0) 
        
        # Dynamically generate the lower-resolution targets
        # We temporarily add a batch dimension for the interpolate function, then remove it
        target_64_batch = target_64.unsqueeze(0)
        
        target_32 = F.interpolate(target_64_batch, size=(32, 32), mode='bilinear', align_corners=False).squeeze(0)
        target_16 = F.interpolate(target_64_batch, size=(16, 16), mode='bilinear', align_corners=False).squeeze(0)
        
        # Return the input geometry and the three grading rubrics
        return sdf, target_16, target_32, target_64

